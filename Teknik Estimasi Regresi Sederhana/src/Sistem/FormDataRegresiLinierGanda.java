    private void simpan_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_btnActionPerformed
        int no         = jumlahdatatabel;
        int noalias    = Integer.parseInt(input_nomor.getText());
        int datax1     = Integer.parseInt(input_data_permintaan_minyak.getText());
        int datay1     = Integer.parseInt(input_data_Harga_minyak.getText());
        int datay2     = Integer.parseInt(input_data_pendapatan.getText());

        try{
            stm = Con.createStatement();
            if(edit==true){
                stm.executeUpdate("UPDATE tbcacat2 SET data_permintaan_minyak="+datax1+", data_harga_minyak="+datay1+", data_pendapatan="+datay2+" WHERE no_sampel="+noalias+" ");
                edit=false;
            }else{
                stm.executeUpdate("INSERT INTO tbcacat2 VALUES ("+no+","+datax1+","+datay1+","+datay2+") ");
            }
            tabelgandaku.setModel(new DefaultTableModel(dataTable,header));
            JOptionPane.showMessageDialog(null, "simpan berhasil");
            baca_data();
            setkosong();
            setdisable();
            tambah_btn.setEnabled(true);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_simpan_btnActionPerformed

