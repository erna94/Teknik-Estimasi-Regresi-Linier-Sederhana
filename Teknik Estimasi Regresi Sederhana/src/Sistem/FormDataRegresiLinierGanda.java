    private void baca_data(){
        try{
            stm = Con.createStatement();
            Rslt = stm.executeQuery("select * from tbcacat2");
            meta = Rslt.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            jumlahdatatabel = 0;
            while(Rslt.next()) {
                
                baris = Rslt.getRow();
                jumlahdatatabel++;
            }
            dataTable = new Object[baris][col];
            int x = 0;
            Rslt.beforeFirst();
            while(Rslt.next()) {
                dataTable[x][0] = Rslt.getInt("no_sampel");
                dataTable[x][1] = Rslt.getInt("data_permintaan_minyak");
                dataTable[x][2] = Rslt.getInt("data_harga_minyak");
                dataTable[x][3] = Rslt.getInt("data_pendapatan");
                x++;
            }
            tabelgandaku.setModel(new DefaultTableModel(dataTable,header));
            jumlahdatatabel=jumlahdatatabel +1;
            input_nomor.setText(""+jumlahdatatabel);
        }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
        }
    }
    
